package co.soft.beans;

public class ContentInfo {
	
	private int con_inx;
	private String con_subject;
	private String con_text;
	private String con_file;
	private int con_writer_idx;
	private int con_board_idx;
	private String con_date;
	
	public int getCon_inx() {
		return con_inx;
	}
	public void setCon_inx(int con_inx) {
		this.con_inx = con_inx;
	}
	public String getCon_subject() {
		return con_subject;
	}
	public void setCon_subject(String con_subject) {
		this.con_subject = con_subject;
	}
	public String getCon_text() {
		return con_text;
	}
	public void setCon_text(String con_text) {
		this.con_text = con_text;
	}
	public String getCon_file() {
		return con_file;
	}
	public void setCon_file(String con_file) {
		this.con_file = con_file;
	}
	public int getCon_writer_idx() {
		return con_writer_idx;
	}
	public void setCon_writer_idx(int con_writer_idx) {
		this.con_writer_idx = con_writer_idx;
	}
	public int getCon_board_idx() {
		return con_board_idx;
	}
	public void setCon_board_idx(int con_board_idx) {
		this.con_board_idx = con_board_idx;
	}
	public String getCon_date() {
		return con_date;
	}
	public void setCon_date(String con_date) {
		this.con_date = con_date;
	}
	
}
